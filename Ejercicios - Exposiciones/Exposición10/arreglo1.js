// Crear un arreglo de frutas
const frutas = ["Manzana", "Pera", "Naranja", "Plátano", "Uva"];

// Acceder a un elemento del arreglo por índice
const primeraFruta = frutas[0]; // primeraFruta = "Manzana"
console.log("Primera fruta:", primeraFruta);

// Acceder al último elemento del arreglo
const ultimaFruta = frutas[frutas.length - 1]; // ultimaFruta = "Uva"
console.log("Última fruta:", ultimaFruta);

// Modificar un elemento del arreglo
frutas[2] = "Mandarina"; // Cambiar "Naranja" por "Mandarina"
console.log("Arreglo modificado:", frutas);